N = 25000;
n = 0:1:N-1;
Fs = 10000;
W1 = 50;
W2 = 10;
M1 = floor(N/W1);
un1 = [ones(1,W1) zeros(1,N-W1)];
un2 = [ones(1,W2) zeros(1,N-W2)];
h1 = cos((pi/4)*n) + cos((pi/2)*n);
h2 = cos((pi/2)*n) + cos((3*pi/4)*n);
w1 = 0.5*(1- cos(2*pi*n/W1)).*un1;
w2 = 0.5*(1- cos(2*pi*n/W2)).*un2;
g1 = h1.*w1;
g2 = h2.*w2;
%imagesc((0:(M1-1))*W1/Fs, (0:(W1-1))/W1, abs(g1));
figure(1);
subplot(2,1,1);
plot(n,abs(fft(g1)));
xlabel('Frequency [Hertz]'); 
ylabel('Magnitude');
title('W1 = 50');
subplot(2,1,2);
plot(n,abs(fft(g2)));
xlabel('Frequency [Hertz]'); 
ylabel('Magnitude');
title('W2 = 10');